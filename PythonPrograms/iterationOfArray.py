
# iterating over a array
import numpy as np
arr=np.array([1,2,3])
for x in arr:
    print(x)

# iterating over 2D array
import numpy as np
arr=np.array([[1,2,3],
             [5,6,8]])
for x in arr:
    print(x)
# iterating over 3D array
import numpy as np
arr=np.array([[[1,2,3],
             [5,6,8],[9,7,4],[10,11,12]]])
for x in arr:
    print(x)